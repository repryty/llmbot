def _weighted_choice(options: List[List]) -> Any:
    total = sum(opt[1] for opt in options)
    r = random.randint(1, total)
    cumulative = 0
    for opt in options:
        cumulative += opt[1]
        if r <= cumulative:
            return opt[0]
    return options[-1][0]


def _finalize(tags: List[str]) -> str:
    seen: set = set()
    result = []
    for tag in tags:
        if tag not in seen:
            seen.add(tag)
            result.append(tag)
    return ", ".join(result)


def generate_appearance(
    seed: Optional[int] = None,
    config: Optional[dict] = None,
) -> str:
    cfg = {**WEIGHT_DEFAULTS, **(config or {})}

    if seed is not None:
        random.seed(seed)

    tags: List[str] = []

    if random.random() < cfg["p_animal"]:
        tags.append(_weighted_choice(ANIMAL_FEATURES))

    if random.random() < cfg["p_skin"]:
        tags.append(_weighted_choice(SKIN_COLORS))

    if random.random() < cfg["p_eye_color"]:
        tags.append(_weighted_choice(EYE_COLORS))

    if random.random() < cfg["p_eye_style"]:
        tags.append(_weighted_choice(EYE_STYLES))

    if random.random() < cfg["p_eye_expr"]:
        tags.append(_weighted_choice(EYE_EXPRESSIONS))

    if random.random() < cfg["p_hair_length"]:
        tags.append(_weighted_choice(HAIR_LENGTHS))

    if random.random() < cfg["p_hair_color"]:
        tags.append(_weighted_choice(HAIR_COLORS))

    if random.random() < cfg["p_hair_multi"]:
        tags.append(_weighted_choice(HAIR_MULTI_COLORS))
        if random.random() < 0.70:
            tags.append(_weighted_choice(HAIR_COLORS))

    if random.random() < cfg["p_braid"]:
        tags.append(_weighted_choice(BRAID_STYLES))

    if random.random() < cfg["p_hair_style"]:
        tags.append(_weighted_choice(HAIR_STYLES))

    if random.random() < cfg["p_bangs"]:
        tags.append(_weighted_choice(BANGS_STYLES))

    if random.random() < cfg["p_hair_acc"]:
        tags.append(_weighted_choice(HAIR_ACCESSORIES))

    if cfg["only_face"]:
        return _finalize(tags)

    # ── Clothing ─────────────────────────────────────────────────────────
    use_dress = random.random() < cfg["p_dress"]
    use_swimwear = (not use_dress) and (random.random() < cfg["p_swimwear"])
    use_bodysuit = (not use_dress) and (not use_swimwear) and (random.random() < cfg["p_bodysuit"])

    if use_dress:
        tags.append(_weighted_choice(CLOTHING_DRESSES))
    elif use_swimwear:
        tags.append(_weighted_choice(CLOTHING_SWIMWEAR))
    elif use_bodysuit:
        tags.append(_weighted_choice(CLOTHING_BODYSUIT))
    else:
        if random.random() < cfg["p_top"]:
            tags.append(_weighted_choice(CLOTHING_TOPS))
        if random.random() < cfg["p_bottom"]:
            tags.append(_weighted_choice(CLOTHING_BOTTOMS))

    if random.random() < cfg["p_outerwear"]:
        tags.append(_weighted_choice(CLOTHING_OUTERWEAR))
    if random.random() < cfg["p_hosiery"]:
        tags.append(_weighted_choice(CLOTHING_HOSIERY))
    if random.random() < cfg["p_footwear"]:
        tags.append(_weighted_choice(CLOTHING_FOOTWEAR))
    if random.random() < cfg["p_headwear"]:
        tags.append(_weighted_choice(CLOTHING_HEADWEAR))
    if random.random() < cfg["p_accessory"]:
        tags.append(_weighted_choice(CLOTHING_ACCESSORIES))
    if cfg["gender"] == "f" and random.random() < cfg["p_breast"]:
        tags.append(_weighted_choice(BREAST_SIZES))

    body_count_options = [[0, 20], [1, 50], [2, 25], [3, 5]]
    n_body = _weighted_choice(body_count_options)
    for _ in range(n_body):
        tags.append(_weighted_choice(BODY_FEATURES))

    if random.random() < cfg["p_expression"]:
        tags.append(_weighted_choice(FACIAL_EXPRESSIONS))

    return _finalize(tags)